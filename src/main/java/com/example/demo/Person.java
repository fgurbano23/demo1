package com.example.demo;

public class Person {
   public String PersonID;
   public String LastName;
   public String FirstName;
   public String Address;
   public String City;

   public Person( String id,String lastname, String name, String address, String City ){
       this.PersonID= id;
       this.LastName = lastname;
       this.FirstName = name;
       this.Address = address;
       this.City = City;

    }
}
